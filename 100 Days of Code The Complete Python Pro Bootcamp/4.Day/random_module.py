import random

# How to create random number (int)
random_number = random.randint(1, 10)
print(random_number)

# How to create random number (float)
random_float = random.random()
print(random_float)

# How to create random number (decimal)
random_decimal = random.uniform(10.5, 22.2)
print(random_decimal)

your_love_score = random.randint(1,100)
print(f"Your love score is {your_love_score}.")